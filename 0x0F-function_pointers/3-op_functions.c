/**
  * op_add - function that returns sum of two ints
  * @a: fist int
  * @b: second int
  *
  * Return: sum of ints
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - function that subtractstwo ints
  * @a: fist int
  * @b: second int
  *
  * Return: int value after subtraction
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - function that returns product of two ints
  * @a: fist int
  * @b: second int
  *
  * Return: product of ints
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - function that returns division  of two ints
  * @a: fist int
  * @b: second int
  *
  * Return: result of division
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - function that returns modulus of two ints
  * @a: fist int
  * @b: second int
  *
  * Return: modulus of ints
  */
int op_mod(int a, int b)
{
	return (a % b);
}
